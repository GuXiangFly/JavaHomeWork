public class Bean {
    int a1;
    int b1;
    int c1;

    public Bean(int a1, int b1, int c1) {
        this.a1 = a1;
        this.b1 = b1;
        this.c1 = c1;
    }

    public int getA1() {
        return a1;
    }

    public void setA1(int a1) {
        this.a1 = a1;
    }

    public int getB1() {
        return b1;
    }

    public void setB1(int b1) {
        this.b1 = b1;
    }

    public int getC1() {
        return c1;
    }

    public void setC1(int c1) {
        this.c1 = c1;
    }
}
